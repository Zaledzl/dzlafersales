package com.zale.aftersales.model;

public class EngineerInfo {
    private String engineerUsername;

    private String engineerPassword;

    private String engineerName;

    private Integer engineerPhoneNumber;

    private String engineerEquipment;

    public String getEngineerUsername() {
        return engineerUsername;
    }

    public void setEngineerUsername(String engineerUsername) {
        this.engineerUsername = engineerUsername;
    }

    public String getEngineerPassword() {
        return engineerPassword;
    }

    public void setEngineerPassword(String engineerPassword) {
        this.engineerPassword = engineerPassword;
    }

    public String getEngineerName() {
        return engineerName;
    }

    public void setEngineerName(String engineerName) {
        this.engineerName = engineerName;
    }

    public Integer getEngineerPhoneNumber() {
        return engineerPhoneNumber;
    }

    public void setEngineerPhoneNumber(Integer engineerPhoneNumber) {
        this.engineerPhoneNumber = engineerPhoneNumber;
    }

    public String getEngineerEquipment() {
        return engineerEquipment;
    }

    public void setEngineerEquipment(String engineerEquipment) {
        this.engineerEquipment = engineerEquipment;
    }
}