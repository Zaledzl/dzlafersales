package com.zale.aftersales.model;

public class EquipmentInfo {
    private String equipmentNumber;

    private String equipmentDoctor;

    private String equipmentEngineer;

    private String equipmentState;

    public String getEquipmentNumber() {
        return equipmentNumber;
    }

    public void setEquipmentNumber(String equipmentNumber) {
        this.equipmentNumber = equipmentNumber;
    }

    public String getEquipmentDoctor() {
        return equipmentDoctor;
    }

    public void setEquipmentDoctor(String equipmentDoctor) {
        this.equipmentDoctor = equipmentDoctor;
    }

    public String getEquipmentEngineer() {
        return equipmentEngineer;
    }

    public void setEquipmentEngineer(String equipmentEngineer) {
        this.equipmentEngineer = equipmentEngineer;
    }

    public String getEquipmentState() {
        return equipmentState;
    }

    public void setEquipmentState(String equipmentState) {
        this.equipmentState = equipmentState;
    }
}